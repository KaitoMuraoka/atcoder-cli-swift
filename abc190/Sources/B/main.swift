// B - Magic 3
// https://atcoder.jp/contests/abc190/tasks/abc190_b

func solve() {
    let (n,s,d) = cin.int3
    var yes = false
    for _ in 0..<n {
        let (x,y) = cin.int2
        if x < s, y > d {
            yes = true
            break
        }
    }
    print(yes ? "Yes" : "No")
}
solve()

